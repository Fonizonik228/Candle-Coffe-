class Command(BaseCommand):
    help = 'Seeds the default menu and creates the admin account (admin / admin123)'

    def handle(self, *args, **options):
        created_count = 0
        for name, category, desc, price, roast in DEFAULT_MENU:
            _, created = Product.objects.get_or_create(
                name=name,
                defaults={'category': category, 'description': desc, 'price': price, 'roast_level': roast},
            )
            if created:
                created_count += 1
        self.stdout.write(self.style.SUCCESS(f'Меню: добавлено {created_count} новых позиций (всего {Product.objects.count()}).'))

        if not User.objects.filter(username='admin').exists():
            admin = User.objects.create_superuser('admin', 'admin@example.com', 'admin123')
            admin.first_name = 'Администратор'
            admin.save()
            self.stdout.write(self.style.SUCCESS('Создан аккаунт администратора: admin / admin123'))
        else:
            self.stdout.write('Аккаунт admin уже существует — пропускаю.')
